# O metodo insert adiciona elementos em uma lista,
# Deve-se indicar (*, **): *posição.
#                         **tem.
# A posição e o item deve estar separados por vírgola.

nomes = ['Ana', 'Carlos', 'Daiane', 'Fernando', 'Maria']
print(nomes)

nomes.pop(2)
nomes.insert(2, 'Jamile')
print(nomes)

nomes.append('Paulo')
print(nomes)