# Iniciando a variavel que recebe um nome.
# O metodo .title() garante que as primeiras letras do nome estarão maiusculas.
nome = input('Digite seu nome:').title()

# Imprime na tela uma saudação que insere a variavel nome.
# O sinal de virgola (,) concatena adicionando um espaço.
# O sinal de mais (+) concatena sem adicionar um espaço.
print('Seja bem vindo', nome + '.')

